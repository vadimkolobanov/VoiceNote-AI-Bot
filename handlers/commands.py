    if NEWS_CHANNEL_URL:
        community_links.append(hlink("üì¢ –ù–æ–≤–æ—Å—Ç–Ω–æ–π –∫–∞–Ω–∞–ª", NEWS_CHANNEL_URL))
    if CHAT_URL:
        community_links.append(hlink("üí¨ –ß–∞—Ç –¥–ª—è –æ–±—Å—É–∂–¥–µ–Ω–∏–π", CHAT_URL))

    community_block = ""
    if community_links:
        community_block = "\n\n" + " | ".join(community_links)

    # --- –ò–ó–ú–ï–ù–ï–ù–ò–ï –í –¢–ï–ö–°–¢–ï ---
    start_text = (
        f"üëã –ü—Ä–∏–≤–µ—Ç, {hbold(message.from_user.first_name)}!\n\n"
        f"–Ø ‚Äî <b>VoiceNote AI</b>, –≤–∞—à –ª–∏—á–Ω—ã–π –ø–æ–º–æ—â–Ω–∏–∫ –¥–ª—è —Å–æ–∑–¥–∞–Ω–∏—è —É–º–Ω—ã—Ö –∑–∞–º–µ—Ç–æ–∫.\n\n"
        f"–ü—Ä–æ—Å—Ç–æ <b>–æ—Ç–ø—Ä–∞–≤—å—Ç–µ –º–Ω–µ –≥–æ–ª–æ—Å–æ–≤–æ–µ</b> –∏–ª–∏ <b>–ø–µ—Ä–µ—à–ª–∏—Ç–µ —Ç–µ–∫—Å—Ç–æ–≤–æ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ</b>, –∏ —è —Å–¥–µ–ª–∞—é –≤—Å—é –º–∞–≥–∏—é –∑–∞ –≤–∞—Å!\n\n"
        f"<b>–ö–∞–∫ —ç—Ç–æ —Ä–∞–±–æ—Ç–∞–µ—Ç?</b>\n"
        f"1Ô∏è‚É£ <b>–ó–∞–ø–∏—à–∏—Ç–µ –∏–ª–∏ –ø–µ—Ä–µ—à–ª–∏—Ç–µ –∏–¥–µ—é:</b> <i>¬´–ù–∞–ø–æ–º–Ω–∏ –∑–∞–≤—Ç—Ä–∞ –∫—É–ø–∏—Ç—å –º–æ–ª–æ–∫–æ –∏ –ø–æ–∑–≤–æ–Ω–∏—Ç—å –º–∞–º–µ –≤ —Å–µ–º—å –≤–µ—á–µ—Ä–∞¬ª</i>\n"
        f"2Ô∏è‚É£ <b>–û—Ç–ø—Ä–∞–≤—å—Ç–µ –º–Ω–µ:</b> –Ø —Ä–∞—Å–ø–æ–∑–Ω–∞—é —Ä–µ—á—å (–¥–ª—è –∞—É–¥–∏–æ) –∏ –ø—Ä–æ–∞–Ω–∞–ª–∏–∑–∏—Ä—É—é —Ç–µ–∫—Å—Ç.\n"
        f"3Ô∏è‚É£ <b>–ü–æ–ª—É—á–∏—Ç–µ —Ä–µ–∑—É–ª—å—Ç–∞—Ç:</b>\n"
        f"<code>"
        f"üìù –ó–∞–¥–∞—á–∞: –ö—É–ø–∏—Ç—å –º–æ–ª–æ–∫–æ, –ø–æ–∑–≤–æ–Ω–∏—Ç—å –º–∞–º–µ\n"
        f"üóìÔ∏è –°—Ä–æ–∫: 13.06.2025 19:00</code>\n"
        f"<i>(–ò –±–æ—Ç –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏ –ø–æ—Å—Ç–∞–≤–∏—Ç –Ω–∞–ø–æ–º–∏–Ω–∞–Ω–∏–µ!)</i>\n\n"
        f"–ò—Å–ø–æ–ª—å–∑—É–π—Ç–µ –∫–Ω–æ–ø–∫–∏ –Ω–∏–∂–µ –¥–ª—è –Ω–∞–≤–∏–≥–∞—Ü–∏–∏ –∏–ª–∏ <b>—Å—Ä–∞–∑—É –æ—Ç–ø—Ä–∞–≤–ª—è–π—Ç–µ —Å–æ–æ–±—â–µ–Ω–∏–µ!</b>"
        f"{timezone_warning}"
        f"{community_block}"
    )

    reply_markup = get_main_menu_keyboard()

    if user_timezone == 'UTC':
        tz_button = types.InlineKeyboardButton(
            text="üïí –ù–∞—Å—Ç—Ä–æ–∏—Ç—å —á–∞—Å–æ–≤–æ–π –ø–æ—è—Å",
            callback_data=SettingsAction(action="go_to_timezone").pack()
        )
        reply_markup.inline_keyboard.append([tz_button])

