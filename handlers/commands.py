@router.message(Command("start"))
async def cmd_start(message: types.Message, state: FSMContext):
    """–û–±—Ä–∞–±–æ—Ç—á–∏–∫ –∫–æ–º–∞–Ω–¥—ã /start —Å —É–ª—É—á—à–µ–Ω–Ω—ã–º –ø—Ä–∏–≤–µ—Ç—Å—Ç–≤–∏–µ–º."""
    await state.clear()
    user_profile = await get_or_create_user(message.from_user)

    # --- –ë–ª–æ–∫ —Å —á–∞—Å–æ–≤—ã–º –ø–æ—è—Å–æ–º (–æ—Å—Ç–∞–µ—Ç—Å—è –±–µ–∑ –∏–∑–º–µ–Ω–µ–Ω–∏–π) ---
    user_timezone = user_profile.get('timezone', 'UTC')
    timezone_warning = ""
    if user_timezone == 'UTC':
        timezone_warning = (
            f"\n\n<b>‚ö†Ô∏è –ù–∞—Å—Ç—Ä–æ–π—Ç–µ —á–∞—Å–æ–≤–æ–π –ø–æ—è—Å!</b>\n"
            f"–ß—Ç–æ–±—ã –Ω–∞–ø–æ–º–∏–Ω–∞–Ω–∏—è –ø—Ä–∏—Ö–æ–¥–∏–ª–∏ –≤–æ–≤—Ä–µ–º—è, –ø–æ–∂–∞–ª—É–π—Å—Ç–∞, "
            f"—É–∫–∞–∂–∏—Ç–µ –≤–∞—à —á–∞—Å–æ–≤–æ–π –ø–æ—è—Å –≤ –Ω–∞—Å—Ç—Ä–æ–π–∫–∞—Ö."
        )

    # --- –ù–û–í–´–ô, –ë–û–õ–ï–ï –ü–û–î–†–û–ë–ù–´–ô –ò –ü–û–õ–ï–ó–ù–´–ô –¢–ï–ö–°–¢ ---
    start_text = (
        f"üëã –ü—Ä–∏–≤–µ—Ç, {hbold(message.from_user.first_name)}!\n\n"
        f"–Ø ‚Äî <b>VoiceNote AI</b>, –≤–∞—à –ª–∏—á–Ω—ã–π –ø–æ–º–æ—â–Ω–∏–∫, –∫–æ—Ç–æ—Ä—ã–π –ø—Ä–µ–≤—Ä–∞—â–∞–µ—Ç –≥–æ–ª–æ—Å–æ–≤—ã–µ —Å–æ–æ–±—â–µ–Ω–∏—è –≤ —Å—Ç—Ä—É–∫—Ç—É—Ä–∏—Ä–æ–≤–∞–Ω–Ω—ã–µ –∑–∞–º–µ—Ç–∫–∏.\n\n"
        f"–ü—Ä–æ—Å—Ç–æ –æ—Ç–ø—Ä–∞–≤—å—Ç–µ –º–Ω–µ –≥–æ–ª–æ—Å–æ–≤–æ–µ, –∏ —è —Å–¥–µ–ª–∞—é –≤—Å—é –º–∞–≥–∏—é –∑–∞ –≤–∞—Å!\n\n"
        f"<b>–ö–∞–∫ —ç—Ç–æ —Ä–∞–±–æ—Ç–∞–µ—Ç?</b>\n"
        f"1Ô∏è‚É£ <b>–ó–∞–ø–∏—à–∏—Ç–µ –∏–¥–µ—é:</b> <i>¬´–ù–∞–ø–æ–º–Ω–∏ –∑–∞–≤—Ç—Ä–∞ –∫—É–ø–∏—Ç—å –º–æ–ª–æ–∫–æ –∏ –ø–æ–∑–≤–æ–Ω–∏—Ç—å –º–∞–º–µ –≤ —Å–µ–º—å –≤–µ—á–µ—Ä–∞¬ª</i>\n"
        f"2Ô∏è‚É£ <b>–û—Ç–ø—Ä–∞–≤—å—Ç–µ –º–Ω–µ:</b> –Ø —Ä–∞—Å–ø–æ–∑–Ω–∞—é —Ä–µ—á—å –∏ –ø—Ä–æ–∞–Ω–∞–ª–∏–∑–∏—Ä—É—é —Ç–µ–∫—Å—Ç.\n"
        f"3Ô∏è‚É£ <b>–ü–æ–ª—É—á–∏—Ç–µ —Ä–µ–∑—É–ª—å—Ç–∞—Ç:</b>\n"
        f"<code>"
        f"üìù –ó–∞–¥–∞—á–∞: –ö—É–ø–∏—Ç—å –º–æ–ª–æ–∫–æ, –ø–æ–∑–≤–æ–Ω–∏—Ç—å –º–∞–º–µ\n"
        f"üóìÔ∏è –°—Ä–æ–∫: 13.06.2025 19:00 </code>\n"
        f"<i>(–ò –±–æ—Ç –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏ –ø–æ—Å—Ç–∞–≤–∏—Ç –Ω–∞–ø–æ–º–∏–Ω–∞–Ω–∏–µ –∑–∞—Ä–∞–Ω–µ–µ –∏ –≤ –º–æ–º–µ–Ω—Ç —Å–æ–±—ã—Ç–∏—è! )</i>\n\n"
        f"–ò—Å–ø–æ–ª—å–∑—É–π—Ç–µ –∫–Ω–æ–ø–∫–∏ –Ω–∏–∂–µ –¥–ª—è –Ω–∞–≤–∏–≥–∞—Ü–∏–∏ –∏–ª–∏ <b>—Å—Ä–∞–∑—É –æ—Ç–ø—Ä–∞–≤–ª—è–π—Ç–µ –≥–æ–ª–æ—Å–æ–≤–æ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ!</b>"
        f"{timezone_warning}"
    )

    # --- –õ–æ–≥–∏–∫–∞ –∫–ª–∞–≤–∏–∞—Ç—É—Ä—ã (–æ—Å—Ç–∞–µ—Ç—Å—è –±–µ–∑ –∏–∑–º–µ–Ω–µ–Ω–∏–π) ---
    reply_markup = get_main_menu_keyboard()
    if user_timezone == 'UTC':
        tz_button = types.InlineKeyboardButton(
            text="üïí –ù–∞—Å—Ç—Ä–æ–∏—Ç—å —á–∞—Å–æ–≤–æ–π –ø–æ—è—Å",
            callback_data=SettingsAction(action="go_to_timezone").pack()
        )
        reply_markup.inline_keyboard.append([tz_button])

    await message.answer(
        start_text,
        reply_markup=reply_markup,
        parse_mode="HTML"
    )


